#!/bin/bash


echo "==============================================================================================================================="
echo "==============================================================================================================================="
echo " MAKING WITH STLPORT(default), destination directory: `pwd`/../distr/"
echo "==============================================================================================================================="
echo "==============================================================================================================================="
#export CCFLAGS=-isystem/usr/include/stlport

make distclean

