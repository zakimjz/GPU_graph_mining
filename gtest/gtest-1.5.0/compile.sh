echo "==============================================================================================================================="
echo "==============================================================================================================================="
echo " MAKING withOUT STLPORT, destination directory: `pwd`/../distr_nostlp/"
echo "==============================================================================================================================="
echo "==============================================================================================================================="

make distclean
export CXXFLAGS=
./configure --prefix=`pwd`/../distr_nostlp/

make -j2

make install


